import UIKit

class MNColors: NSObject {

    static func bg1() -> UIColor {
        return UIColor.init(white: 1, alpha: 1)
    }
    
    static func bg2() -> UIColor {
        return UIColor.init(white: 0.7, alpha: 1)
    }

    static func bg3() -> UIColor {
        return UIColor.init(white: 0.3, alpha: 1)
    }

    static func bg4() -> UIColor {
        return UIColor.init(white: 0.97, alpha: 1)
    }
}
